// Function to get current language from localStorage or default to 'uz'
function getCurrentLanguage() {
  return localStorage.getItem("traveluz-language") || "uz"
}

// Function to set current language
function setCurrentLanguage(lang) {
  localStorage.setItem("traveluz-language", lang)
}

// Function to get translation
function t(key) {
  const currentLang = getCurrentLanguage()
  return translations[currentLang][key] || translations["en"][key] || key
}

// Function to update all text elements on the page
function updatePageTranslations() {
  // Update all elements with translation IDs
  const elementsToTranslate = document.querySelectorAll("[id]")

  elementsToTranslate.forEach((element) => {
    const id = element.id
    const translationKey = getTranslationKey(id)

    if (translationKey && t(translationKey) !== translationKey) {
      if (
        element.tagName === "INPUT" &&
        (element.type === "text" || element.type === "email" || element.type === "tel" || element.type === "password")
      ) {
        element.placeholder = t(translationKey)
      } else {
        element.textContent = t(translationKey)
      }
    }
  })
}

